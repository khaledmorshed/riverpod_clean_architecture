class User {
  final String id;
  final String displayName;
  final String email;
  final String phone;
  final String token;
  final String roleId;
  final String warehouseId;

  User({
    required this.id,
    required this.displayName,
    required this.email,
    required this.phone,
    required this.token,
    required this.roleId,
    required this.warehouseId,
  });
}
